#include <unistd.h>

void	ft_strputchar(char c)
{
	write (1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_strputchar(str[i]);
		i++;
	}
}
